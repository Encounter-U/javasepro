public class ArrayDemo2
    {
        /**
         * 数组遍历
         * @param args
         */
        public static void main(String[] args)
            {
                int[] arr={12,13,14};
                for (int i = 0; i < arr.length; i++)
                    {
                        System.out.print(arr[i]+" ");
                    }
            }
    }
